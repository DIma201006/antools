document.getElementById('button-like-1').addEventListener('click', function () {
        if (document.getElementById('like-1').classList.contains('active')) {
            document.getElementById('like-1').classList.remove('active');
            document.getElementById('red-like-1').classList.add('active');
            return
        } else 
        document.getElementById('red-like-1').classList.remove('active');
        document.getElementById('like-1').classList.add('active'); 
     });
    
